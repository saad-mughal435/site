function bindEditorInputs() {
    // Bind right-sidebar simple inputs to draft.export_details
    document.querySelectorAll('#customs-export-editor .ce-input').forEach(inp => {
        inp.addEventListener('input', e => {
            const key = e.target.dataset.key;
            if (!key) return;
            let v = e.target.value;
            if (e.target.type === 'number') v = v === '' ? null : parseFloat(v);
            setNested(exportEditorState.draft.export_details, key, v);
            if (key.startsWith('charges.') || key === 'value_aed') refreshEditorTotals();
        });
    });

    // Container numbers
    const cont = document.getElementById('ce-containers');
    if (cont) {
        cont.addEventListener('input', e => {
            exportEditorState.draft.export_details.container_numbers =
                e.target.value.split('\n').map(s => s.trim()).filter(Boolean);
        });
    }

    // Product select autofills price + HS code + unit
    const sel = document.getElementById('ce-product-select');
    if (sel) {
        sel.addEventListener('change', e => {
            const opt = e.target.selectedOptions[0];
            if (!opt || !opt.value) return;
            const price = opt.dataset.price;
            const hs = opt.dataset.hs;
            const unit = opt.dataset.unit;
            const priceEl = document.getElementById('ce-add-price');
            const unitEl = document.getElementById('ce-add-unit');
            if (priceEl && price !== '' && price != null) priceEl.value = price;
            if (unitEl && unit) unitEl.value = unit;
            // autofill HS code if blank
            if (!exportEditorState.draft.export_details.hs_code && hs) {
                exportEditorState.draft.export_details.hs_code = hs;
                const hsInp = document.querySelector('#customs-export-editor [data-key="hs_code"]');
                if (hsInp) hsInp.value = hs;
            }
        });
    }
}

function addEditorItem() {
    if (!exportEditorState) return;
    const code = document.getElementById('ce-product-select')?.value;
    if (!code) {
        showToast('Pick a product first', 'error');
        return;
    }
    const product = (exportEditorState.products || []).find(p => p.item_code === code);
    const qty = parseFloat(document.getElementById('ce-add-qty').value || '0');
    if (!qty || qty <= 0) {
        showToast('Enter a quantity', 'error');
        return;
    }
    const priceRaw = document.getElementById('ce-add-price').value;
    const price = priceRaw === '' ? (product?.unit_price_aed ?? null) : parseFloat(priceRaw);
    const unit = document.getElementById('ce-add-unit').value || 'Case';

    exportEditorState.draft.items.push({
        item_code: code,
        description: product?.description || '',
        quantity: qty,
        unit,
        unit_price_aed: price,
    });

    // Reset add form
    document.getElementById('ce-product-select').value = '';
    document.getElementById('ce-add-qty').value = 1;
    document.getElementById('ce-add-price').value = '';

    refreshEditorItemsTable();
    refreshEditorTotals();
}

function removeEditorItem(idx) {
    if (!exportEditorState) return;
    exportEditorState.draft.items.splice(idx, 1);
    refreshEditorItemsTable();
    refreshEditorTotals();
}

function refreshEditorItemsTable() {
    const tbody = document.getElementById('ce-items-body');
    if (!tbody || !exportEditorState) return;
    const items = exportEditorState.draft.items || [];
    if (!items.length) {
        tbody.innerHTML = `<tr><td colspan="7" class="px-4 py-6 text-center text-slate-400 text-sm">No items yet. Add products above.</td></tr>`;
        return;
    }
    tbody.innerHTML = items.map((it, idx) => {
        const qty = Number(it.quantity || 0);
        const price = Number(it.unit_price_aed || 0);
        const total = qty * price;
        return `
        <tr class="border-t border-slate-100 hover:bg-slate-50/50">
            <td class="px-3 py-2 text-xs font-medium text-slate-700">${it.item_code || ''}</td>
            <td class="px-3 py-2 text-xs text-slate-600">${it.description || ''}</td>
            <td class="px-3 py-2 text-right">
                <input type="number" step="any" class="ce-item-qty w-20 text-right border border-slate-300 rounded px-2 py-1 text-xs" data-idx="${idx}" value="${qty}">
            </td>
            <td class="px-3 py-2 text-right">
                <input type="number" step="any" class="ce-item-price w-24 text-right border border-slate-300 rounded px-2 py-1 text-xs" data-idx="${idx}" value="${it.unit_price_aed ?? ''}">
            </td>
            <td class="px-3 py-2 text-center">
                <input type="text" class="ce-item-unit w-16 text-center border border-slate-300 rounded px-2 py-1 text-xs" data-idx="${idx}" value="${it.unit || 'Case'}">
            </td>
            <td class="px-3 py-2 text-right text-xs font-medium text-slate-800">${total.toLocaleString(undefined, {minimumFractionDigits: 2, maximumFractionDigits: 2})}</td>
            <td class="px-3 py-2 text-center">
                <button onclick="window.__removeEditorItem(${idx})" class="text-red-400 hover:text-red-600">
                    <svg class="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M6 18L18 6M6 6l12 12"></path></svg>
                </button>
            </td>
        </tr>`;
    }).join('');

    // Bind per-row edits
    tbody.querySelectorAll('.ce-item-qty').forEach(inp => {
        inp.addEventListener('input', e => {
            const i = parseInt(e.target.dataset.idx, 10);
            exportEditorState.draft.items[i].quantity = parseFloat(e.target.value || '0');
            refreshEditorItemsTable();
            refreshEditorTotals();
        });
    });
    tbody.querySelectorAll('.ce-item-price').forEach(inp => {
        inp.addEventListener('input', e => {
            const i = parseInt(e.target.dataset.idx, 10);
            const v = e.target.value;
            exportEditorState.draft.items[i].unit_price_aed = v === '' ? null : parseFloat(v);
            refreshEditorItemsTable();
            refreshEditorTotals();
        });
    });
    tbody.querySelectorAll('.ce-item-unit').forEach(inp => {
        inp.addEventListener('input', e => {
            const i = parseInt(e.target.dataset.idx, 10);
            exportEditorState.draft.items[i].unit = e.target.value;
        });
    });
}

function refreshEditorTotals() {
    if (!exportEditorState) return;
    const items = exportEditorState.draft.items || [];
    const qtySum = items.reduce((s, it) => s + Number(it.quantity || 0), 0);
    const amtSum = items.reduce((s, it) => s + Number(it.quantity || 0) * Number(it.unit_price_aed || 0), 0);
    const qtyEl = document.getElementById('ce-total-qty');
    const amtEl = document.getElementById('ce-total-amount');
    if (qtyEl) qtyEl.textContent = qtySum.toLocaleString();
    if (amtEl) amtEl.textContent = 'AED ' + amtSum.toLocaleString(undefined, {minimumFractionDigits: 2, maximumFractionDigits: 2});
}

async function saveAndGenerateExportPdf() {
    if (!exportEditorState || !currentDeclaration) return;
    const { docKind, draft } = exportEditorState;

    if (!draft.export_details.pi_number) {
        showToast('Proforma Invoice No. is required', 'error');
        return;
    }
    if (!draft.items.length) {
        showToast('Add at least one product', 'error');
        return;
    }

    // Auto-fill ex_work and value_aed from items if blank
    const itemsTotal = draft.items.reduce(
        (s, it) => s + Number(it.quantity || 0) * Number(it.unit_price_aed || 0), 0);
    if (draft.export_details.charges == null) draft.export_details.charges = {};
    if (!draft.export_details.charges.ex_work) draft.export_details.charges.ex_work = itemsTotal;
    if (!draft.export_details.value_aed) {
        const c = draft.export_details.charges;
        draft.export_details.value_aed = (Number(c.ex_work || 0) + Number(c.insurance || 0) +
            Number(c.freight_and_handling || 0) + Number(c.transport || 0));
    }

    try {
        // Save details + items first
        const putRes = await authenticatedFetch(`/api/customs/declarations/${currentDeclaration._id}`, {
            method: 'PUT',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ export_details: draft.export_details, items: draft.items }),
        });
        if (!putRes.ok) throw new Error('Failed to save');

        // Generate PDF
        showToast('Generating PDF...', 'info');
        const res = await authenticatedFetch(
            `/api/customs/declarations/${currentDeclaration._id}/generate/${docKind}`,
            { method: 'POST' },
        );
        if (!res.ok) {
            let msg = 'Generation failed';
            try { msg = (await res.json()).detail || msg; } catch (_) {}
            throw new Error(msg);
        }
        const blob = await res.blob();
        const url = URL.createObjectURL(blob);
        window.open(url, '_blank');
        setTimeout(() => URL.revokeObjectURL(url), 30000);

        showToast(`${DOC_TITLES[docKind]} generated`, 'success');
        // Refresh underlying declaration data, then return to detail
        await viewDeclaration(currentDeclaration._id);
        await loadDeclarations();
        closeExportEditor();
    } catch (err) {
        console.error(err);
        showToast('Error: ' + err.message, 'error');
    }
}

