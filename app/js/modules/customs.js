async function uploadFile(inputEl, documentType) {
    if (!inputEl.files.length || !currentDeclaration) return;
    const file = inputEl.files[0];
    const formData = new FormData();
    formData.append('file', file);
    formData.append('document_type', documentType);

    try {
        const token = localStorage.getItem('demoplant_auth_token');
        const res = await fetch(`/api/customs/declarations/${currentDeclaration._id}/files`, {
            method: 'POST',
            headers: { 'Authorization': `Bearer ${token}` },
            body: formData,
        });
        if (!res.ok) throw new Error('Upload failed');
        showToast(`Uploaded ${file.name}`, 'success');
        await viewDeclaration(currentDeclaration._id);
        await loadDeclarations();
    } catch (err) {
        console.error(err);
        showToast('Error uploading file', 'error');
    }
    inputEl.value = '';
}

async function downloadFile(fileId, filename) {
    try {
        const res = await authenticatedFetch(`/api/customs/files/${fileId}`);
        if (!res.ok) throw new Error('Download failed');
        const blob = await res.blob();
        const url = URL.createObjectURL(blob);
        const a = document.createElement('a');
        a.href = url;
        a.download = filename;
        document.body.appendChild(a);
        a.click();
        URL.revokeObjectURL(url);
        a.remove();
    } catch (err) {
        console.error(err);
        showToast('Error downloading file', 'error');
    }
}

async function deleteFile(fileId) {
    if (!confirm('Delete this file?')) return;
    try {
        const res = await authenticatedFetch(`/api/customs/files/${fileId}`, { method: 'DELETE' });
        if (!res.ok) throw new Error('Delete failed');
        showToast('File deleted', 'success');
        if (currentDeclaration) await viewDeclaration(currentDeclaration._id);
        await loadDeclarations();
    } catch (err) {
        console.error(err);
        showToast('Error deleting file', 'error');
    }
}

