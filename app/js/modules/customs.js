export async function loadCustomsPage() {
    const now = new Date();
    const monthSel = document.getElementById('customs-new-month');
    if (monthSel) monthSel.value = String(now.getMonth() + 1);
    const yearInp = document.getElementById('customs-new-year');
    if (yearInp) yearInp.value = String(now.getFullYear());

    const ftaMonthSel = document.getElementById('fta-month');
    if (ftaMonthSel) ftaMonthSel.value = String(now.getMonth() + 1);
    const ftaYearInp = document.getElementById('fta-year');
    if (ftaYearInp) ftaYearInp.value = String(now.getFullYear());

    switchCustomsSubTab(currentSubTab);
}

