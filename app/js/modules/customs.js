async function viewDeclaration(id) {
    try {
        const res = await authenticatedFetch(`/api/customs/declarations/${id}`);
        if (!res.ok) {
            const body = await res.text().catch(() => '');
            throw new Error(`Fetch failed (${res.status}): ${body.slice(0, 200)}`);
        }
        currentDeclaration = await res.json();
    } catch (err) {
        console.error('viewDeclaration fetch error:', err);
        showToast('Error loading declaration: ' + err.message, 'error');
        return;
    }
    try {
        renderDeclarationDetail();
    } catch (err) {
        console.error('renderDeclarationDetail error:', err);
        showToast('Error rendering declaration: ' + err.message, 'error');
    }
}

function closeDeclarationDetail() {
    currentDeclaration = null;
    exportEditorState = null;
    const panel = document.getElementById('customs-detail-panel');
    if (panel) panel.classList.add('hidden');
    const editor = document.getElementById('customs-export-editor');
    if (editor) editor.classList.add('hidden');
}

function renderDeclarationDetail() {
    const panel = document.getElementById('customs-detail-panel');
    if (!panel || !currentDeclaration) return;
    panel.classList.remove('hidden');

    const d = currentDeclaration;
    document.getElementById('customs-detail-title').textContent =
        `${d.declaration_number} — ${TYPE_LABELS[d.declaration_type] || d.declaration_type}`;
    document.getElementById('customs-detail-subtitle').textContent =
        `${MONTH_NAMES[d.month] || d.month} ${d.year} | Status: ${(d.status || 'draft').replace('_', ' ')}`;

    // Documents checklist
    const docsEl = document.getElementById('customs-detail-documents');
    const requiredTypes = d.required_document_types || [];
    const uploadedFiles = d.files || [];

    if (!requiredTypes.length && !uploadedFiles.length) {
        docsEl.innerHTML = `<p class="text-slate-400 text-sm">No document types defined for this declaration type yet.</p>`;
    } else {
        const typeSet = new Set(requiredTypes);
        uploadedFiles.forEach(f => typeSet.add(f.document_type));

        const rows = Array.from(typeSet).map(docType => {
            const matchingFiles = uploadedFiles.filter(f => f.document_type === docType);
            const isRequired = requiredTypes.includes(docType);
            const uploaded = matchingFiles.length > 0;
            const generateKind = GENERATED_DOC_KINDS[docType];
            const canGenerate = !!generateKind && d.declaration_type === 'export_other_country';

            let statusBadge;
            if (uploaded) {
                statusBadge = `<span class="inline-flex items-center gap-1 text-emerald-600 text-xs font-medium"><svg class="w-3.5 h-3.5" fill="currentColor" viewBox="0 0 20 20"><path fill-rule="evenodd" d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z" clip-rule="evenodd"/></svg>${matchingFiles.some(f => f.generated) ? 'Generated' : 'Uploaded'}</span>`;
            } else if (isRequired) {
                statusBadge = `<span class="text-amber-600 text-xs font-medium">Missing</span>`;
            } else {
                statusBadge = `<span class="text-slate-400 text-xs">Optional</span>`;
            }

            const filesList = matchingFiles.map(f => {
                const fname = (f.filename || 'file').toString();
                const fnameEsc = fname.replace(/'/g, "\\'");
                return `
                <div class="flex items-center gap-2 mt-1 ml-4">
                    <a href="#" onclick="event.preventDefault(); window.__downloadFile('${f._id}', '${fnameEsc}')" class="text-sm text-blue-600 hover:underline truncate">${fname}</a>
                    <span class="text-xs text-slate-400">${formatFileSize(f.size || 0)}</span>
                    ${f.generated ? '<span class="text-[10px] text-blue-500 uppercase font-semibold">App-generated</span>' : ''}
                    <button onclick="window.__deleteFile('${f._id}')" class="text-red-400 hover:text-red-600">
                        <svg class="w-3.5 h-3.5" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M6 18L18 6M6 6l12 12"></path></svg>
                    </button>
                </div>`;
            }).join('');

            const openEditorBtn = canGenerate ? `
                    <button onclick="window.__openExportEditor('${generateKind}')" class="inline-flex items-center gap-1.5 px-3 py-1.5 bg-blue-600 text-white border border-blue-600 rounded-lg text-xs font-medium hover:bg-blue-700">
                        <svg class="w-3.5 h-3.5" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M15.232 5.232l3.536 3.536m-2.036-5.036a2.5 2.5 0 113.536 3.536L6.5 21.036H3v-3.572L16.732 3.732z"></path></svg>
                        Open Editor
                    </button>` : '';

            return `
            <div class="flex flex-col p-3 rounded-lg border ${uploaded ? 'border-emerald-200 bg-emerald-50/30' : isRequired ? 'border-amber-200 bg-amber-50/30' : 'border-slate-200'}">
                <div class="flex items-center justify-between">
                    <div class="flex items-center gap-2">
                        <span class="text-sm font-medium text-slate-800">${docType}</span>
                        ${statusBadge}
                    </div>
                    <div class="flex items-center gap-2">
                        ${openEditorBtn}
                        ${canGenerate ? '' : `<label class="inline-flex items-center gap-1.5 px-3 py-1.5 bg-white border border-slate-300 rounded-lg text-xs font-medium text-slate-700 hover:bg-slate-50 cursor-pointer">
                            <svg class="w-3.5 h-3.5" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M4 16v1a3 3 0 003 3h10a3 3 0 003-3v-1m-4-8l-4-4m0 0L8 8m4-4v12"></path></svg>
                            Upload
                            <input type="file" class="hidden" onchange="window.__uploadFile(this, '${docType.replace(/'/g, "\\'")}')" accept=".pdf,.jpg,.jpeg,.png,.doc,.docx,.xls,.xlsx">
                        </label>`}
                    </div>
                </div>
                ${filesList}
            </div>`;
        }).join('');

        docsEl.innerHTML = rows;
    }

    // Notes
    const notesEl = document.getElementById('customs-detail-notes');
    if (notesEl) notesEl.value = d.notes || '';

    panel.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

