let currentSubTab = 'export_other_country';
let declarations = [];
let currentDeclaration = null;

const MONTH_NAMES = ['', 'January', 'February', 'March', 'April', 'May', 'June',
    'July', 'August', 'September', 'October', 'November', 'December'];

const TYPE_LABELS = {
    export_other_country: 'Export (Other Country)',
    export_mainland: 'Export (Mainland)',
    fta: 'FTA',
};

const STATUS_COLORS = {
    draft: 'bg-slate-100 text-slate-700',
    in_progress: 'bg-amber-100 text-amber-700',
    submitted: 'bg-blue-100 text-blue-700',
    completed: 'bg-emerald-100 text-emerald-700',
};

// Documents that can be generated by the app (export_other_country only)
const GENERATED_DOC_KINDS = {
    'Proforma Invoice': 'proforma_invoice',
    'Packing List': 'packing_list',
    'Delivery Advice': 'delivery_advice',
};

// ============================================================================
// Sub-tab switching
// ============================================================================

export function switchCustomsSubTab(tab) {
    currentSubTab = tab;
    const tabs = ['export_other_country', 'export_mainland', 'fta'];
    const activeClass = 'px-5 py-2.5 font-medium text-sm rounded-lg bg-blue-600 text-white shadow-sm border border-slate-300';
    const inactiveClass = 'px-5 py-2.5 font-medium text-sm rounded-lg bg-white text-slate-700 border border-slate-300 hover:bg-slate-50';

    tabs.forEach(t => {
        const btn = document.getElementById(`customs-subtab-${t}`);
        if (btn) btn.className = t === tab ? activeClass : inactiveClass;
    });

    const ftaPanel = document.getElementById('fta-excel-panel');
    const declPanel = document.getElementById('customs-declarations-panel');
    if (tab === 'fta') {
        if (ftaPanel) ftaPanel.classList.remove('hidden');
        if (declPanel) declPanel.classList.add('hidden');
        loadFtaTransferRefs();
    } else {
        if (ftaPanel) ftaPanel.classList.add('hidden');
        if (declPanel) declPanel.classList.remove('hidden');
    }

    closeDeclarationDetail();
    if (tab !== 'fta') loadDeclarations();
}

