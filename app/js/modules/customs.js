async function createDeclaration() {
    const type = document.getElementById('customs-new-type')?.value;
    const month = parseInt(document.getElementById('customs-new-month')?.value, 10);
    const year = parseInt(document.getElementById('customs-new-year')?.value, 10);
    const notes = document.getElementById('customs-new-notes')?.value || '';

    if (!type || !month || !year) {
        showToast('Please fill all fields', 'error');
        return;
    }

    try {
        const res = await authenticatedFetch('/api/customs/declarations', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ declaration_type: type, month, year, notes }),
        });
        if (!res.ok) throw new Error('Failed to create');
        showToast('Declaration created', 'success');
        closeNewDeclarationModal();
        document.getElementById('customs-new-notes').value = '';
        currentSubTab = type;
        switchCustomsSubTab(type);
    } catch (err) {
        console.error(err);
        showToast('Error creating declaration', 'error');
    }
}

async function deleteDeclaration(id) {
    if (!confirm('Delete this declaration and all its files?')) return;
    try {
        const res = await authenticatedFetch(`/api/customs/declarations/${id}`, { method: 'DELETE' });
        if (!res.ok) throw new Error('Failed to delete');
        showToast('Declaration deleted', 'success');
        closeDeclarationDetail();
        loadDeclarations();
    } catch (err) {
        console.error(err);
        showToast('Error deleting declaration', 'error');
    }
}

