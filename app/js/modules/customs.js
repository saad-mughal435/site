async function showAddItemsModal() {
    if (!currentDeclaration) return;
    const modal = document.getElementById('customs-items-modal');
    if (modal) {
        modal.classList.remove('hidden');
        modal.classList.add('flex');
    }

    const listEl = document.getElementById('customs-ts-items-list');
    if (listEl) listEl.innerHTML = '<div class="text-center text-slate-400 py-4">Loading TS items...</div>';

    try {
        const res = await authenticatedFetch(
            `/api/customs/ts-items?month=${currentDeclaration.month}&year=${currentDeclaration.year}`
        );
        if (!res.ok) throw new Error('Failed');
        const data = await res.json();
        const items = data.items || [];

        const existingRefs = new Set((currentDeclaration.items || []).map(i => i.ts_reference));

        if (!items.length) {
            listEl.innerHTML = '<div class="text-center text-slate-400 py-4">No TS items found for this period.</div>';
            return;
        }

        listEl.innerHTML = items.map((item, idx) => {
            const already = existingRefs.has(item.ts_reference);
            return `
            <label class="flex items-center gap-3 p-3 rounded-lg border border-slate-200 hover:bg-slate-50 ${already ? 'opacity-50' : ''}">
                <input type="checkbox" class="customs-ts-checkbox rounded border-slate-300" data-idx="${idx}" ${already ? 'disabled checked' : ''}>
                <div class="flex-1 min-w-0">
                    <span class="font-medium text-blue-600">${item.ts_reference}</span>
                    <span class="text-slate-500 mx-2">|</span>
                    <span class="text-sm text-slate-700">${item.item_code}</span>
                    <span class="text-slate-400 mx-1">-</span>
                    <span class="text-sm text-slate-600 truncate">${item.description || ''}</span>
                </div>
                <span class="text-sm font-medium text-slate-700">${item.quantity}</span>
            </label>`;
        }).join('');

        window.__tsItemsCache = items;
    } catch (err) {
        console.error(err);
        listEl.innerHTML = '<div class="text-center text-red-500 py-4">Error loading TS items.</div>';
    }
}

function closeAddItemsModal() {
    const modal = document.getElementById('customs-items-modal');
    if (modal) {
        modal.classList.add('hidden');
        modal.classList.remove('flex');
    }
}

async function addSelectedItems() {
    if (!currentDeclaration || !window.__tsItemsCache) return;

    const checkboxes = document.querySelectorAll('.customs-ts-checkbox:checked:not(:disabled)');
    const newItems = [];
    checkboxes.forEach(cb => {
        const idx = parseInt(cb.dataset.idx, 10);
        const item = window.__tsItemsCache[idx];
        if (item) {
            newItems.push({
                item_code: item.item_code,
                description: item.description,
                quantity: item.quantity,
                batch_no: item.batch_no,
                ts_reference: item.ts_reference,
            });
        }
    });

    if (!newItems.length) {
        showToast('No items selected', 'error');
        return;
    }

    const merged = [...(currentDeclaration.items || []), ...newItems];

    try {
        const res = await authenticatedFetch(`/api/customs/declarations/${currentDeclaration._id}`, {
            method: 'PUT',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ items: merged }),
        });
        if (!res.ok) throw new Error('Failed');
        showToast(`Added ${newItems.length} item(s)`, 'success');
        closeAddItemsModal();
        await viewDeclaration(currentDeclaration._id);
    } catch (err) {
        console.error(err);
        showToast('Error adding items', 'error');
    }
}

async function removeItem(idx) {
    if (!currentDeclaration) return;
    const items = [...(currentDeclaration.items || [])];
    items.splice(idx, 1);

    try {
        const res = await authenticatedFetch(`/api/customs/declarations/${currentDeclaration._id}`, {
            method: 'PUT',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ items }),
        });
        if (!res.ok) throw new Error('Failed');
        showToast('Item removed', 'success');
        await viewDeclaration(currentDeclaration._id);
    } catch (err) {
        console.error(err);
        showToast('Error removing item', 'error');
    }
}

