function getNested(obj, path) {
    return path.split('.').reduce((o, k) => (o == null ? o : o[k]), obj);
}
function setNested(obj, path, value) {
    const keys = path.split('.');
    let cur = obj;
    for (let i = 0; i < keys.length - 1; i++) {
        if (cur[keys[i]] == null || typeof cur[keys[i]] !== 'object') cur[keys[i]] = {};
        cur = cur[keys[i]];
    }
    cur[keys[keys.length - 1]] = value;
}

async function fetchProductCatalog() {
    if (productCatalogCache) return productCatalogCache;
    try {
        const [fgRes, plRes, pmRes] = await Promise.all([
            authenticatedFetch('/api/finished-goods-inventory').catch(() => null),
            authenticatedFetch('/api/price-list?limit=1000').catch(() => null),
            authenticatedFetch('/api/customs/product-master').catch(() => null),
        ]);
        const fgData = fgRes && fgRes.ok ? await fgRes.json() : [];
        const plData = plRes && plRes.ok ? await plRes.json() : { items: [] };
        const pmData = pmRes && pmRes.ok ? await pmRes.json() : { products: [] };

        const priceMap = {};
        (plData.items || []).forEach(p => { priceMap[p.item_code] = p; });
        const masterMap = {};
        (pmData.products || []).forEach(p => { masterMap[p.item_code] = p; });

        const fgList = Array.isArray(fgData) ? fgData : (fgData.items || []);
        productCatalogCache = fgList.map(fg => {
            const code = fg['Item Code'] || fg.item_code || '';
            const desc = fg['Item Description'] || fg.description || '';
            const price = priceMap[code]?.base_price ?? masterMap[code]?.unit_price_aed ?? null;
            return {
                item_code: code,
                description: desc,
                unit_price_aed: price,
                hs_code: masterMap[code]?.hs_code || '',
                unit: priceMap[code]?.uom || 'Case',
            };
        }).filter(p => p.item_code);

        // Append any product-master entries not present in finished goods, so users can still pick them
        const seen = new Set(productCatalogCache.map(p => p.item_code));
        (pmData.products || []).forEach(p => {
            if (!seen.has(p.item_code)) {
                productCatalogCache.push({
                    item_code: p.item_code,
                    description: p.description || '',
                    unit_price_aed: p.unit_price_aed ?? null,
                    hs_code: p.hs_code || '',
                    unit: 'Case',
                });
            }
        });

        productCatalogCache.sort((a, b) => a.item_code.localeCompare(b.item_code));
        return productCatalogCache;
    } catch (e) {
        console.error('Error fetching product catalog', e);
        return [];
    }
}

